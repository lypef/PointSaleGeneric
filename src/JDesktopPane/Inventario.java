    private void Print() 
    {
        try {
            MessageFormat Header = new MessageFormat("LISTADO DE INVENTARIO");
            TablaBusqueda.print(JTable.PrintMode.FIT_WIDTH, Header, null, false, null, isSelected, null);
        } catch (PrinterException ex) {
            Logger.getLogger(Store.class.getName()).log(Level.SEVERE, null, ex);
        } catch (HeadlessException ex) {
            Logger.getLogger(Store.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showInternalMessageDialog(Desktop.Escritorio,"Hecho");
    }

