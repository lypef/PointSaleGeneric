    private void BtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddActionPerformed
        if (add)
        {
            if (f.Family_Add(TxtNombre, TxtDescripcion))
            {
                f.Familys_Get(Tabla);
                f.Alert("Familia agregada", functions.Alert_Informacion);
            }
        }else
        {
            if (f.Family_update(TxtNombre, TxtDescripcion, id))
            {
                clean();
                f.Alert("Familia actualizada", functions.Alert_Informacion);
            }
        }
        
    }//GEN-LAST:event_BtnAddActionPerformed

    private void TablaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMousePressed
        if (evt.getClickCount() == 2)
        {
            id = Integer.parseInt((String) f.Lista_Familia.get(Tabla.getSelectedRow()));
            
            ButtonGroup Group = new ButtonGroup();

            
            JCheckBox chec0 = new JCheckBox("Ninguno");
            Group.add(chec0);
            chec0.setSelected(true);
            
            
            JCheckBox chec1 = new JCheckBox("Editar");
            Group.add(chec1);
            
            
            JCheckBox chec2 = new JCheckBox("Eliminar");
            Group.add(chec2);
            
           
            int seleccion = JOptionPane.showOptionDialog( this,"Sleccione una opcion",
                "EDICION FAMILIA",JOptionPane.YES_NO_CANCEL_OPTION,
                JOptionPane.INFORMATION_MESSAGE,null,
                new Object[] { "ACEPTAR",chec0, chec1, chec2},"ACEPTAR");

            if (chec1.isSelected())
            {
                add = false;
                f.Family_Edit_Get(TxtNombre, TxtDescripcion, id);
                BtnAdd.setText("Actualizar");
            }
            
            if (chec2.isSelected())
            {
                if (JOptionPane.showConfirmDialog(rootPane,"Â¿Eliminar elemento?","Eliminar",0, functions.Alert_Warning)==0)
                {
                    if (f.Family_Delete(id))
                    {
                        f.Familys_Get(Tabla);
                        f.Alert("Familia eliminada", functions.Alert_Informacion);
                    }
                }
                
                
            }

            
        }
    }//GEN-LAST:event_TablaMousePressed

